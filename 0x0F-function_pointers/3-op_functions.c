#include <stdlib.h>
/**
 *op_add - function that adds two integers
 *@a: first int
 *@b: second int
 *
 *Return: sum_add
 */


int op_add(int a, int b)
{
sum_add = a + b;
return (sum_add);
}

/**
 *op_sub - function that subtracts two integers
 *@a: first int
 *@b: second int
 *
 *Return: sum_sub
 */


int op_sub(int a, int b)
{
sum_sub = a - b;
return (sum_sub);
}

/**
 *op_mul - function that multiplies two integers
 *@a: first int
 *@b: second int
 *
 *Return: sum_multi
 */

int op_mul(int a, int b)
{
sum_multi = a * b;
return (sum_multi);
}

/**
 *op_div - function that divides two integers
 *@a: first int
 *@b: second int
 *
 *Return: sum_div
 */


int op_div(int a, int b)
{
sum_div = a / b;
return (sum_div);
}

/**
 *op_mod - function that prints the remainder of two integers
 *@a: first int
 *@b: second int
 *
 *Return: sum_mod
 */

int op_mod(int a, int b)
{
sum_mod = a % b;
return (sum_mod);
}
